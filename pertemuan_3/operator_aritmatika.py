x = 5
y = 3

# Penjumlahan
print("Penjumlahan=", x + y)

# Pengurangan (-)
print("Pengurangan=", x - y)
# Perkalian(*)
print("Perkalian=", x * y)
# Pembagian(/)
print("Pembagian=", x / y)
# Modulus (%)
print("Modulus=", x % y)
# Perpangkatan (**)
print("Perpangkatan=", x ** y)
# Pembagian bulat (//) 
print("Pembagian bulat=", x // y)